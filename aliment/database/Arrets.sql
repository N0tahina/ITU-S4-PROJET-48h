CREATE TABLE ligne(
    idligne INT PRIMARY KEY,
    nom varchar(4)
);

CREATE TABLE arret(
    nom VARCHAR(100),
    latitude numeric,
    longitude numeric,
    idligne INT REFERENCES ligne(idligne)
);

CREATE OR REPLACE VIEW v_arret AS SELECT arret.nom AS lieu,arret.latitude,arret.longitude,arret.idligne,ligne.nom FROM arret join ligne on ligne.idligne=arret.idligne;

